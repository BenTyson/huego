// Convert sRGB to linear RGB
function srgbToLinear(c: number): number {
  c = c / 255;
  return c <= 0.04045 ? c / 12.92 : Math.pow((c + 0.055) / 1.055, 2.4);
}

// RGB to OKLab conversion
function rgbToOklab(r: number, g: number, b: number): { L: number; a: number; b: number } {
  const lr = srgbToLinear(r);
  const lg = srgbToLinear(g);
  const lb = srgbToLinear(b);

  const l = 0.4122214708 * lr + 0.5363325363 * lg + 0.0514459929 * lb;
  const m = 0.2119034982 * lr + 0.6806995451 * lg + 0.1073969566 * lb;
  const s = 0.0883024619 * lr + 0.2817188376 * lg + 0.6299787005 * lb;

  const l_ = Math.cbrt(l);
  const m_ = Math.cbrt(m);
  const s_ = Math.cbrt(s);

  return {
    L: 0.2104542553 * l_ + 0.793617785 * m_ - 0.0040720468 * s_,
    a: 1.9779984951 * l_ - 2.428592205 * m_ + 0.4505937099 * s_,
    b: 0.0259040371 * l_ + 0.7827717662 * m_ - 0.808675766 * s_,
  };
}

// Parse hex to RGB
function hexToRgb(hex: string): { r: number; g: number; b: number } {
  return {
    r: parseInt(hex.slice(0, 2), 16),
    g: parseInt(hex.slice(2, 4), 16),
    b: parseInt(hex.slice(4, 6), 16),
  };
}

// Generate the database file
function generateDatabase() {
  const entries: string[] = [];

  for (const [hex, name] of rawColors) {
    const rgb = hexToRgb(hex);
    const lab = rgbToOklab(rgb.r, rgb.g, rgb.b);

    entries.push(
      `  { hex: "#${hex}", name: "${name}", lab: { L: ${lab.L.toFixed(6)}, a: ${lab.a.toFixed(6)}, b: ${lab.b.toFixed(6)} } }`
    );
  }

  const output = `/**
 * Named Colors Database
 * Auto-generated from Name That Color data
 * Contains ${entries.length} colors with pre-computed OKLab values
 */

export interface NamedColor {
  hex: string;
  name: string;
  lab: { L: number; a: number; b: number };
}

export const namedColors: NamedColor[] = [
${entries.join(",\n")}
];
`;

  const outputPath = path.join(__dirname, "../src/lib/color-names/database.ts");
  fs.writeFileSync(outputPath, output, "utf-8");
  console.log(`Generated ${outputPath} with ${entries.length} colors`);
}

